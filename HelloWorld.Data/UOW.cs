using System;

namespace HelloWorld.Data
{
    public class Uow : IDisposable
    {
        //----------------------------------
        DataContext db = null;
        //----------------------------------
        public HelloWorldRepository HelloWorlds { get; set; }
       
        //----------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------  CONSTRUCTOR
        public Uow(DataContext DB)
        {
            //--------------------
            if (db == null)
                db = DB;
            //---------------------------------------------------------------
            HelloWorlds = new HelloWorldRepository(db);
            //---------------------------------------------------------------
        }
        //----------------------------------------------------------------------------------------------
        //------------------------------------------------------------------------------  COMMIT CHANGES
        public void Commit()
        {
            db.SaveChanges();
        }
        //----------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------
        #region Disposable
        //----------------------------------------------------------------------------------------------
        private bool disposed = false;
        //--------------------------------------
        protected virtual void Dispose(bool disposing)
        {
            if (!this.disposed)
            {
                if (disposing)
                {
                    db.Dispose();
                }
            }
            this.disposed = true;
        }
        //--------------------------------------
        public void Dispose()
        {
            Dispose(true);
            GC.SuppressFinalize(this);
        }
        //----------------------------------------------------------------------------------------------
        #endregion
        //----------------------------------------------------------------------------------------------
    }
}
